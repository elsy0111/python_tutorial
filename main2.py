#*-------------- 四則演算 --------------*#

a = 3
b = 2
print(a + b)  # 5 足す
print(a - b)  # 1 引く
print(a * b)  # 6 かける (a x b)
print(a / b)  # 1.5 割る (a ÷ b)
print(a ** b) # 9 べき乗 (a^b)

s1 = "abc"
s2 = "def"
print(s1 + s2) # abcdef 文字列同士は結合

s = "sth"
a = 3
print(s + str(a)) # sth3 整数型(int型)のaを文字列型(str型)にして足す

a = "5"
b = "2"
print(int(a) + int(b)) # 7 文字列型(str型)のa,bを整数型(int型)にして足す (同様に四則演算が可能)

