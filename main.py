#*------------- いろんな型 -------------*#

# 文字列型 : int
# 浮動小数点型 : float
# 整数型 : str

n = 1
print(type(n)) # <class 'int'> Pythonは動的型付け言語(推定してくれる！)

n = 1.0
print(type(n)) # <class 'float'> 〃

n = int(1.5)
print(type(n)) # <class 'int'>
print(n)       # 1

s = "sth"
print(type(s)) # <class 'str'>

